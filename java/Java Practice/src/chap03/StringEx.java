package chap03;

public class StringEx {

	public static void main(String[] args) {
		String name;  // 변수 선언
		name = "홍길동";  // 변수 초기화
		String name2 = "홍길동";  // 선언과 동시에 초기화
		String name3 = null;  // null값으로 선언
		String name4 = "";  // ""값으로 선언
		System.out.println(name+name2+name3+name4);  //홍길동홍길동null공백
		
	}

}
